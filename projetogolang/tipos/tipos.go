package main

import "fmt"

func main() {
	//bool (true/false)
	fmt.Printf("Type: %T - Value: %v\n", true, true)
	fmt.Printf("Type: %T - Value: %v\n", false, false)

	//string - sequencia de bytes
	fmt.Printf("Type: %T - Value: %v\n", "Kaenna", "Kaenna")

	//int
	fmt.Printf("Type: %T - Value: %v\n", 1, 1)

	//float
	fmt.Printf("Type: %T - Value: %v\n", 1.23, 1.23)

}

//Tipos:
//bool(true/false)
// string - sequencia de bytes
//int
// float (float64/float32) - decimal
